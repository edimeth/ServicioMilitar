public void Actualizar(){
    
    
int Compania;

try{
    

resultado = Conexion.consulta("Select [ID_Compania] from [dbo].[Compania]");

while(resultado.next()){
  
    Compania = resultado.getInt(1);
    
    CmbCompan.addItem(Compania);
}

}catch(SQLException Ex){
    
}

int Cuerpo;

try{
    

resultado = Conexion.consulta("Select [ID_Cuerpo] from [dbo].[Cuerpo]");

while(resultado.next()){
  
    Cuerpo= resultado.getInt(1);
    
    CmbCuartel.addItem(Cuerpo);
}

}catch(SQLException Ex){
    
}


}
  

